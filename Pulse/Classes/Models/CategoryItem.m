#import "CategoryItem.h"

@implementation CategoryItem

- (id)initwithDictionary:(NSDictionary*)dic{
    if (self == [super init]) {
        self.itemID = [dic objectForKey:@"id"];
        self.cat_id = [dic objectForKey:@"id"];
        self.cat_name = [NSString stringWithFormat:@"%@",[dic objectForKey:@"cat_name"]];
    }
    return self;
}


@end
